    /**
     * Checks for spanish palindromes (since there are no english ones)
     * @param array1 english array
     * @param array2 spanish array
     * @param iteration nth longest palindrome
     */
    private static void palindrome(String[] array1, String[] array2, int iteration) {
        String[] unsortedArray = new String[array1.length];
        System.arraycopy(array1, 0, unsortedArray, 0, array1.length);
        String[] unsortedArray2 = new String[array2.length];
        System.arraycopy(array2, 0, unsortedArray2, 0, array2.length);
        sort(array1, array1.length);
        List<String> palindromes = new ArrayList<>();
        for (String s : array1) {
            if (isPalindromeHelper(s)) {
                palindromes.add(s);
            }
        }
        //Sorting the palindroems in decreasing order
        Collections.sort(palindromes, (s1, s2) -> s2.length() - s1.length());
        List<String> arraySpanish = new ArrayList<>(Arrays.asList(unsortedArray));
        System.out.printf("%-5d %-17s %s \n", arraySpanish.indexOf(palindromes.get(iteration)) + 1, unsortedArray[arraySpanish.indexOf(palindromes.get(iteration))], unsortedArray2[arraySpanish.indexOf(palindromes.get(iteration))]);
    }

    /**
     * Helper function for palindrome that checks if a stirng is a palindrome
     * @param s string that is checked to be a palindrome
     * @return true for palindrome, false for semordnilap
     */
    private static boolean isPalindromeHelper(String s) {
        int left = 0;
        int right = s.length() - 1;
        while (left < right) {
            if (s.charAt(left)!= s.charAt(right)) {
                return false;
            }
            left++;
            right--;
        }
        return true;
    }
